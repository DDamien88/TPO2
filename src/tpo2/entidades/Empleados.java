package tpo2.entidades;

/**
 *
 * @author Carlos Baeza
 */
public class Empleados {
    private String nombre;
    private int edad;
    public Empleados(String nombre, int edad) {
        this.nombre = nombre;
        this.edad = edad;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }
    
}
